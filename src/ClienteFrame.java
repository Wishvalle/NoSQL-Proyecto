    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        cliente = Procedimientos.BuscarCliente(Integer.parseInt(this.txtBuscarID.getText()));
        if(cliente != null){
            JOptionPane.showMessageDialog(null, "¡Cliendo encontrado!" , "Confirmacion", JOptionPane.INFORMATION_MESSAGE);
            this.txtNombre.setText(cliente.getNombre());
            this.txtApellido.setText(cliente.getApellido());
            this.txtCedula.setText(cliente.getCedula());
            this.txtTelefono.setText(cliente.getTelefono());
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        System.out.println(cliente.getId());
        cliente.setNombre(this.txtNombre.getText());
        cliente.setApellido(this.txtApellido.getText());
        cliente.setCedula(this.txtCedula.getText());
        cliente.setTelefono(this.txtTelefono.getText());
        Procedimientos.ModificarCliente(cliente, cliente.getId());
        Listar();
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int row = jTable1.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(null, "Seleccione primero el registro que desea eliminar.");
        } else {

            int opc = JOptionPane.showConfirmDialog(this, "¿Esta seguro de eliminar este registro?", "Pregunta",
                JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (opc == JOptionPane.YES_OPTION) {
                System.out.println(Integer.parseInt(jTable1.getValueAt(row, 0).toString()));
                Procedimientos.EliminarCliente(Integer.parseInt(jTable1.getValueAt(row, 0).toString()));
                
            }
            Listar();
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
       Cliente document = new Cliente();
       document.setNombre(this.txtNombre.getText());
       document.setApellido(this.txtApellido.getText());
       document.setCedula(this.txtCedula.getText());
       document.setTelefono(this.txtTelefono.getText());
       Procedimientos.AgregarCliente(document);
       this.txtNombre.setText("");
       this.txtApellido.setText("");
       this.txtCedula.setText("");
       this.txtTelefono.setText("");
       Listar();
    }//GEN-LAST:event_btnAgregarActionPerformed


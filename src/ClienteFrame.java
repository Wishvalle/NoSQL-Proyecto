    public void Listar(){
      DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
      modelo.setRowCount(0);
      clientes = Procedimientos.ListarClientes();
        for (int i = 0; i < clientes.size(); i++) {
            Vector v = new Vector();
            v.add(clientes.get(i).getId());
            v.add(clientes.get(i).getNombre());
            v.add(clientes.get(i).getApellido());
            v.add(clientes.get(i).getCedula());
            v.add(clientes.get(i).getTelefono());
            modelo.addRow(v);
            jTable1.setModel(modelo);
        }
    }
